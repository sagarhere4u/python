#mathfunctions.py
var1 = 10.50
var2 = 20.70

import math
print ("Absolute value of", var1, abs(var1))
print ("Exponential value of", var1, math.exp(var1))

print("Largest in given number", max(var1, var2))
print("Smallest in given number", min(var1, var2))

print("Power of 2 for var1", pow(var1, 2))
print("Round value of var1", round(var1, 2))

print ("Square of 9 is ", math.sqrt(9))